def build_reverse_map():
    reverse = {}
    for canonical, aliases in STANDARD_COLUMNS.items():
        for alias in aliases:
            normalized = _normalize_str(alias)
            reverse[normalized] = canonical
    return reverse


def _normalize_str(s: str) -> str:
    return (
        s.strip()
        .replace("　", "")
        .replace("（", "(")
        .replace("）", ")")
        .lower()
    )
